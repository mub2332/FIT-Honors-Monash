package com.fit3170.allocation.services;

public class UniversityLocation {
    private String university;
    private String city;
    private String state;
    private int zip;

    public UniversityLocation(String university, String city, String state, int zip) {
        this.university = university;
        this.city = city;
        this.state = state;
        this.zip = zip;
    }
}
